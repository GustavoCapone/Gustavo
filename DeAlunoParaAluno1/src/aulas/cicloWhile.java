package aulas;

public class cicloWhile {
    public static void main(String[]args){
        //WHILE
        //Vem o controle depois Executa
        int i = 1;
        while(i< 20){
            System.out.println(i);
            i++;
            
        }
        
        //DO WHILE
        //Executa depois vem o controle
        
        int x = 1;
        
        do{
            System.out.println(x);
            x++;
        }while(x < 10);
        
        
        
        
    }
}
